    private List<Usuario> cargaCombo(){
        List<Usuario> lta = null;
        try{
        lta = usuarioSER.getAll();
        }catch(Exception ex){
            SessionApp.muestraError("Error al cargar Usuarios\nRevise la conexion");
            System.exit(1);
        }
        if(lta != null){
            if(lta.isEmpty()){
                this.jButton1.setEnabled(false);
                SessionApp.muestraError("No existen Usuarios");
            }
        }
        else{
            SessionApp.muestraError("Error al cargar Usuarios");
            System.exit(1);
        }
        return lta;
    }
