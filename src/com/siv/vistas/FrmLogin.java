    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(this.jPasswordField1.getPassword().length!=0 && !"".equals(this.jTextField1.getText().trim())){
            String pass = new String(this.jPasswordField1.getPassword());
            String usuario = this.jTextField1.getText();
            String id_sistema = SessionApp.os.getId();
            Usuario user = null;
            try{
                user = this.usuarioSER.getUser(usuario, pass, id_sistema);
            }catch(Exception ex){
                SessionApp.muestraError("Error al Loguearse");
                this.jPasswordField1.requestFocus();
            }
            if(user != null){
                SessionApp.user = user;
                Logueo logeo = new Logueo();
                InetAddress h = null;
                String host = "";
                try{
                    h = InetAddress.getLocalHost();
                    host = h.getHostName();
                }catch(Exception ex){
                    host = "error al obtener nombre de host";
                }
                logeo.setUsuario(user);
                logeo.setHost(host);
                logeo.setIp(SessionApp.ip);
                String fi = format.format(new Date());
                logeo.setFecha_inicio(fi);
                logueoSER.insert(logeo);
                SessionApp.fecha_inicio = fi;
                if(user.getTipo().toLowerCase().equals("administrador")){
                    FrmPrincipal frm = new FrmPrincipal();
                    frm.setTitle("Sistema de Inventario y Venta");
                    frm.setLocationRelativeTo(null);
                    frm.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    frm.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    frm.setVisible(true);
                }else{
                    FrmPrincipalNoAdmin frm = new FrmPrincipalNoAdmin();
                    frm.setTitle("Sistema de Inventario y Venta");
                    frm.setLocationRelativeTo(null);
                    frm.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    frm.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    frm.setVisible(true);
                }
                this.dispose();
            }
        }
        else{
            SessionApp.muestraAdvertencia("usuario y/o Clave en blanco");
            this.jPasswordField1.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
