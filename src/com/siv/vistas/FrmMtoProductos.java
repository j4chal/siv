    private void buscar_en_Filas(String buscado){
        carga_Tabla();
        if(!"".equals(buscado)){
            DefaultTableModel tb_ant = (DefaultTableModel) this.jTable1.getModel();
            int j=0;
            int LIM = jTable1.getRowCount();
            while(0<LIM){
                if(!tb_ant.getValueAt(j,1).toString().toLowerCase().startsWith(buscado.toLowerCase())){
                    tb_ant.removeRow(j);
                }
                else{
                    j++;
                }
                LIM--;
            }
            this.jTable1.setModel(tb_ant);
        }
    }
    
    private void selecciona_en_combo_estado(String estado){
        for(int i=0;i<this.jcboEstado.getItemCount();i++){
            String ca = this.jcboEstado.getItemAt(i).toString();
            if(ca.toLowerCase().equals(estado.toLowerCase())){
                this.jcboEstado.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void selecciona_en_combo_categoria(Categoria cat){
        for(int i=0;i<this.jcboCategoria.getItemCount();i++){
            Categoria ca = null;
            try
            {
                ca = (Categoria)this.jcboCategoria.getItemAt(i);
            }catch(Exception e){
                continue;
            }
            if(ca.getDescripcion().toLowerCase().equals(cat.getDescripcion().toLowerCase())){
                this.jcboCategoria.setSelectedIndex(i);
                break;
            }
        }
    }
    
    private void carga_Tabla(){
        String cols[] = {"ID","NOMBRE"};
        List<Producto> lta = null;
        try{
            lta = productoSER.getAll();
        }catch(Exception e){
            SessionApp.muestraError("No se pudo cargar los datos");
            return ;
        }
        Object[][] rows = new Object[lta.size()][cols.length];
        for(int i=0;i<lta.size();i++){
            rows[i][0] = lta.get(i).getId();
            rows[i][1] = lta.get(i).getNombre();
        }
        DefaultTableModel tb = new DefaultTableModel(rows,cols){
            @Override
            public boolean isCellEditable(int row,int col){
                return false;
            }
        };
        jTable1.setModel(tb);
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_SUBSEQUENT_COLUMNS);
    }
    
