    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        // TODO add your handling code here:
        if(!"".equals(this.jtNombre.getText().trim()) && this.jcboCategoria.getSelectedIndex()!=-1 && this.jcboEstado.getSelectedIndex()!=-1
                && !"".equals(this.jtMarca.getText()) && !"".equals(this.jtUnidadMedida.getText()) && !"".equals(this.jtCantidad.getText())
                && !"".equals(this.jtPrecioBase.getText()) && !"".equals(this.jtPrecioGral.getText())){
            Producto p = new Producto();
            p.setNombre(jtNombre.getText());
            p.setCategoria((Categoria) this.jcboCategoria.getSelectedItem());
            p.setEstado(this.jcboEstado.getSelectedItem().toString());
            p.setGanancia(Double.parseDouble(this.jtUtilidad.getText()));
            p.setMarca(this.jtMarca.getText());
            p.setPrecio_base(Double.parseDouble(this.jtPrecioBase.getText()));
            p.setPrecio_gral(Double.parseDouble(this.jtPrecioGral.getText()));
            p.setStock(Double.parseDouble(this.jtCantidad.getText()));
            p.setUnidad_medida(this.jtUnidadMedida.getText());
            int rpta = 0;
            try{
                if(this.jbtnNuevo.isEnabled()){
                    rpta = productoSER.insert(p);
                }
                else{
                    p.setId(Integer.parseInt(this.jtCodigo.getText()));
                    rpta = productoSER.update(p);
                }
            }catch(Exception e){
                SessionApp.muestraError("Error #100-40\nContácte al Administrador del Sistema");
                return ;
            }
            if(rpta == 0){
                SessionApp.muestraAdvertencia("Error al realizar la operación");
            }
            else{
                this.jbtnCancelar.doClick();
                this.carga_Tabla();
            }
        }
        else{
            SessionApp.muestraAdvertencia("Por Favor...\nComplete todos los Datos");
        }
    }//GEN-LAST:event_jbtnGuardarActionPerformed

