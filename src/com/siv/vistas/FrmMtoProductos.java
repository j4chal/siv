    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int fila_selected = this.jTable1.getSelectedRow();
        int id = Integer.parseInt(this.jTable1.getValueAt(fila_selected, 0).toString());
        Producto p = null;
        try{
            p = productoSER.getProducto(id);
        }catch(Exception e){
            SessionApp.muestraError("Error #100-40\nContácte al Administrador del Sistema");
            return ;
        }
        DecimalFormat format = new DecimalFormat("#.##");
        this.jtCodigo.setText(p.getId().toString());
        this.jtNombre.setText(p.getNombre());
        this.jtMarca.setText(p.getMarca());
        this.jtUnidadMedida.setText(p.getUnidad_medida());
        this.jtCantidad.setText(format.format(p.getStock()));
        this.jtPrecioBase.setText(""+p.getPrecio_base());
        this.jtPrecioGral.setText(""+p.getPrecio_gral());
        this.jtUtilidad.setText(format.format(p.getGanancia()));
        this.selecciona_en_combo_categoria(p.getCategoria());
        this.selecciona_en_combo_estado(p.getEstado());
        this.activa_no_btnnnuevo();
        this.jbtnNuevo.setEnabled(false);
        this.activa_cajas(true);
    }//GEN-LAST:event_jTable1MouseClicked

