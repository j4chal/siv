    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()==2){
            int fila_selected = this.jTable1.getSelectedRow();
            int id = Integer.parseInt(this.jTable1.getValueAt(fila_selected, 0).toString());
            Venta p = null;
            try{
                p = ventaSER.getVenta(id);
            }catch(Exception e){
                SessionApp.muestraError("Error #100-40\nContácte al Administrador del Sistema");
            }
            if(p!=null){
                if(!checkState(FrmMtoCategoria.class)){
                    FrmDetalle frm = new FrmDetalle();
                    frm.setP(p);
                    JDesktopPane jdk = this.getDesktopPane();
                    jdk.add(frm);
                    frm.setMaximizable(false);
                    frm.setIconifiable(true);
                    frm.setResizable(false);
                    frm.moveToFront();
                    try{
                        frm.setSelected(true);
                    }catch(Exception e){}
                        frm.show();
                    }else{
                        SessionApp.muestraAdvertencia("Cierre la ventana de Detalle\npara poder abrir otra");
                    }
                }else{
                SessionApp.muestraError("Error #100-40\nContácte al Administrador del Sistema");
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

