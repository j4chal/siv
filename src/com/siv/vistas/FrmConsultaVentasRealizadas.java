    private boolean checkState(Class x){
        JDesktopPane jdk = this.getDesktopPane();
        JInternalFrame[] frames = jdk.getAllFrames();
        if(frames!=null && frames.length>0){
            for(JInternalFrame jif : frames){
                if(jif.getClass() == x){
                    return true;
                }
            }
        }
        return false;
    }
    
