    private void buscar_en_Filas(String buscado){
        carga_Tabla();
        if(!"".equals(buscado)){
            DefaultTableModel tb_ant = (DefaultTableModel) this.jTable1.getModel();
            int j=0,k=0;
            if(this.jComboBox1.getSelectedItem().toString().equalsIgnoreCase("codigo")){
                k=0;
            }
            if(this.jComboBox1.getSelectedItem().toString().equalsIgnoreCase("categoria")){
                k=1;
            }
            if(this.jComboBox1.getSelectedItem().toString().equalsIgnoreCase("producto")){
                k=2;
            }
            if(this.jComboBox1.getSelectedItem().toString().equalsIgnoreCase("marca")){
                k=3;
            }
            int LIM = jTable1.getRowCount();
            while(0<LIM){
                if(!tb_ant.getValueAt(j,k).toString().toLowerCase().contains(buscado.toLowerCase())){
                    tb_ant.removeRow(j);
                }
                else{
                    j++;
                }
                LIM--;
            }
            this.jTable1.setModel(tb_ant);
        }
    }
    
    private void carga_Tabla(){
        String cols[] = {"CODIGO","CATEGORIA","PRODUCTO","MARCA","UNIDAD MEDIDA","STOCK","PRECIO COMPRA","PRECIO VENTA","UTILIDAD"};
        List<Producto> lta = null;
        try{
            lta = productoSER.getAll();
        }catch(Exception e){
            SessionApp.muestraError("No se pudo cargar los datos");
            return ;
        }
        Object[][] rows = new Object[lta.size()][cols.length];
        for(int i=0;i<lta.size();i++){
            rows[i][0] = lta.get(i).getId();
            rows[i][1] = lta.get(i).getCategoria().getDescripcion();
            rows[i][2] = lta.get(i).getNombre();
            rows[i][3] = lta.get(i).getMarca();
            rows[i][4] = lta.get(i).getUnidad_medida();
            rows[i][5] = lta.get(i).getStock();
            rows[i][6] = lta.get(i).getPrecio_base();
            rows[i][7] = lta.get(i).getPrecio_gral();
            rows[i][8] = lta.get(i).getGanancia();
        }
        DefaultTableModel tb = new DefaultTableModel(rows,cols){
            @Override
            public boolean isCellEditable(int row,int col){
                return false;
            }
        };
        jTable1.setModel(tb);
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_SUBSEQUENT_COLUMNS);
    }

