    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        // TODO add your handling code here:
        if(!"".equals(this.jTextField1.getText().trim())){
            int igv = 0;
            try{
                igv = Integer.parseInt(this.jTextField1.getText());
                if(igv<3){
                    SessionApp.muestraAdvertencia("Ingrese correctamente el IGV");
                    return ;
                }
                int rpta = 0;
                if(error_load){
                    rpta = productoSER.insertIGV(igv);
                }else{
                    rpta = productoSER.updateIGV(igv);
                }
                if(rpta == 0){
                    SessionApp.muestraAdvertencia("No se logro cambiar el IGV");
                }
                else{
                    this.jTextField1.setText(productoSER.getIGV()+"");
                    this.jLabel3.setText("Cambio Realizado");
                }
            }catch(Exception e){
                SessionApp.muestraAdvertencia("Ingrese correctamente el IGV");
                return ;
            }
        }
        else{
            SessionApp.muestraAdvertencia("Ingrese el IGV");
            return ;
        }
    }//GEN-LAST:event_jbtnGuardarActionPerformed

