    private void jbtnquitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnquitarActionPerformed
        // TODO add your handling code here:
        if(this.jTable1.getRowCount()>0){
            int fila = this.jTable1.getSelectedRow();
            String pp = this.jTable1.getValueAt(fila, 0).toString();
            int idP = this.productoSER.getProductoByNombre(pp).getId();
            DetalleVenta px = null;
            for(DetalleVenta d : this.ltaDetalles){
                if(d.getProducto().getId()==idP){
                    px = d;
                    break;
                }
            }
            if(px != null){
                this.ltaDetalles.remove(px);
            }
            this.cargaDetalles();
            this.jtSubtotal.setText(formateador.format(this.sumarMontos()).toString());
        }else{
            SessionApp.muestraAdvertencia("Selecciona un producto de la lista\npara poder quitarla");
        }
    }//GEN-LAST:event_jbtnquitarActionPerformed

