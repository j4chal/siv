    private void genera_nroVenta(){
        String lastNroVenta = this.ventaSER.getLastNroVenta();
        if(!lastNroVenta.equals("")){
            String[] nro_venta_last = lastNroVenta.split("-");
            int nro_sgte = Integer.parseInt(nro_venta_last[0]);
            int ultimoNro = Integer.parseInt(nro_venta_last[1])+1;
            String sgteNroVenta = "";
            String sgteNro = "";
            if(ultimoNro<10){
                sgteNroVenta = "00000"+ultimoNro;
            }
            else if(ultimoNro<100){
                sgteNroVenta = "0000"+ultimoNro;
            }
            else if(ultimoNro<1000){
                sgteNroVenta = "000"+ultimoNro;
            }
            else if(ultimoNro<10000){
                sgteNroVenta = "00"+ultimoNro;
            }
            else if(ultimoNro<100000){
                sgteNroVenta = "0"+ultimoNro;
            }
            else if(ultimoNro<1000000){
                sgteNroVenta = ""+ultimoNro;
            }
            else{
                sgteNro += 1;
            }
            if(nro_sgte<10){
                sgteNro = "000"+nro_sgte;
            }else if(nro_sgte<100){
                sgteNro = "00"+nro_sgte;
            }else if(nro_sgte<1000){
                sgteNro = "0"+nro_sgte;
            }else if(nro_sgte<10000){
                sgteNro = ""+nro_sgte;
            }
            this.jtNroVenta.setText(sgteNro+"-"+sgteNroVenta);
        }else{
            this.jtNroVenta.setText("0001-000001");
        }
    }

