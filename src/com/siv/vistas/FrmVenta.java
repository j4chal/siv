    private void jbtnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGenerarActionPerformed
        // TODO add your handling code here:
        if(!this.ltaDetalles.isEmpty()){
            if(this.cliente_deBusqueda!=null){
                Venta v = new Venta();
                v.setCliente(cliente_deBusqueda);
                v.setDescuento(Double.parseDouble(this.jtDescuento.getText()));
                v.setFecha(new SimpleDateFormat("yyyy-MM-dd hh:mm:ss").format(new Date()));
                v.setIgv((double)productoSER.getIGV());
                v.setNro_venta(this.jtNroVenta.getText());
                v.setSubtotal(Double.parseDouble(this.jtSubtotal.getText()));
                v.setTotal(Double.parseDouble(this.jtTotal.getText()));
                v.setVendedor(SessionApp.user);
                int rpta = 0;
                rpta = ventaSER.insert(v, ltaDetalles);
                if(rpta == 0){
                    SessionApp.muestraError("Error al Generar la Venta");
                }else{
                    JOptionPane.showMessageDialog(null, "Se genero la venta", "Venta Realizada", JOptionPane.INFORMATION_MESSAGE);
                }
                this.jbtnCancelar.doClick();
                this.jbtnNuevo.doClick();
            }else{
                SessionApp.muestraAdvertencia("Debe escoger un cliente");
            }
        }else{
            SessionApp.muestraAdvertencia("No hay productos en lista");
        }
    }//GEN-LAST:event_jbtnGenerarActionPerformed

