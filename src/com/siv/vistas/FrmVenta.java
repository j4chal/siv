    private void cargaDetalles(){
        if(this.ltaDetalles!=null){
            String[] cols = {"PRODUCTO","CANTIDAD","TOTAL"};
            Object[][] rows = new Object[this.ltaDetalles.size()][3];
            for(int i=0;i<ltaDetalles.size();i++){
                rows[i][0] = ltaDetalles.get(i).getProducto().getNombre();
                rows[i][1] = ltaDetalles.get(i).getCantidad();
                rows[i][2] = formateador.format(ltaDetalles.get(i).getTotal());
            }
            DefaultTableModel tb = new DefaultTableModel(rows,cols){
                @Override
                public boolean isCellEditable(int r,int f){
                    return false;
                }
            };
            this.jTable1.setModel(tb);
        }
    }
    
    private void jbtnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnagregarActionPerformed
        // TODO add your handling code here:
        if(this.producto!=null){
            if(Integer.parseInt(this.jsCantidad.getValue().toString())!=0){
                DetalleVenta dt = new DetalleVenta();
                dt.setProducto(producto);
                dt.setCantidad(Double.parseDouble(this.jsCantidad.getValue().toString()));
                dt.setTotal(dt.getCantidad()*producto.getPrecio_gral());
                this.ltaDetalles.add(dt);
                this.cargaDetalles();
                this.producto=null;
                this.jtProducto.setText("");
                this.jtStock.setText("");
                this.jsCantidad.setValue(0);
                this.jtSubtotal.setText(formateador.format(this.sumarMontos()).toString());
            }else{
                SessionApp.muestraAdvertencia("Revise la Cantidad del producto");
            }
        }
        else{
            SessionApp.muestraAdvertencia("Seleccione un producto");
        }
    }//GEN-LAST:event_jbtnagregarActionPerformed

    private double sumarMontos(){
        double suma = 0;
        if(this.jTable1.getRowCount()>0){
            DefaultTableModel tb = (DefaultTableModel) this.jTable1.getModel();
            for(int i=0;i<tb.getRowCount();i++){
                double monto = 0;
                try{
                    monto = Double.parseDouble(tb.getValueAt(i, 2).toString());
                }catch(Exception e){
                    monto = 0;
                }
                suma += monto;
            }
        }
        return suma;
    }
    
