    private void actualizaTotal(){
        if(!"".equals(this.jtSubtotal.getText())){
            double descuento = 0;
            try{
                descuento = Double.parseDouble(this.jtDescuento.getText());
                if(descuento < 0){
                    //SessionApp.muestraAdvertencia("Error con el descuento...Revise el valor\nSe establecerá valor 0");
                    descuento = 0;
                }
            }catch(Exception e){
                //SessionApp.muestraAdvertencia("Error con el descuento...Revise el valor\nSe establecerá valor 0");
                descuento = 0;
            }
            double st = Double.parseDouble(this.jtSubtotal.getText());
            int igv = this.productoSER.getIGV();
            double monto_igv = st*0.18;
            double totalX = 0;
            totalX = st+monto_igv;
            if(this.jCheckBox1.isSelected()){
                totalX = totalX - (totalX*(descuento/100));
            }
            else{
                totalX = totalX - descuento;
            }
            this.jtTotal.setText(formateador.format(totalX));
        }
    }
    
